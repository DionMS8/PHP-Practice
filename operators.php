//=====[OPERATORS]=================================================================================

//===[COMPARISON OPERATORS]=========================================================================

== — EQUAL IN VALUE
=== — IDENTICAL
!= — NOT EQUAL IN VALUE
<> — NOT EQUAL IN VALUE
!== — NOT IDENTICAL
< — LESS THAN
> — GREATER THAN
<= — LESS THAN OR EQUAL TO
>= — GREATER THAN OR EQUAL TO
<=> — LESS THAN, EQUAL TO, OR GREATER THAN


//===[LOGICAL OPERATORS]===============================================================================

and — AND
or — OR
xor — EXCLUSIVE OR
! — NOT
&& — AND
|| — OR





